  layout_height='250dp';--布局高度
  gravity="center";
  {
    CardView;--卡片控件
    layout_margin='16';--卡片边距
    CardElevation='2';--卡片阴影
    layout_width='fill';--卡片宽度
    layout_height='fill';--卡片高度
    radius='16';--卡片圆角
    CardBackgroundColor='#ffffff';--卡片背景颜色
    {
      LinearLayout;--线性布局
      Orientation='vertical';--布局方向
      layout_width='fill';--布局宽度
