{
  LinearLayout;--线性布局
  Orientation='vertical';--布局方向
  layout_width='fill';--布局宽度
  layout_height='fill';--布局高度
  background='#ff03a9f4';--布局背景颜色(或图片路径)
  {
    Toolbar;
    Title="任务";
    TitleTextColor="#ffffff";
  };
  {
    GridView;--网格控件
    id="grid";
    layout_width='fill';--网格宽度
    layout_height='fill';--网格高度
    Gravity='center';--对齐方式
    NumColumns='2';--列数
    --ColumnWidth='';--每一列的宽度
    --StretchMode='';--缩放
    HorizontalSpacing='16';--两列直接的边距
